func getField[Taddr: static RegType](regVal: RegVal[Taddr], bitOffset: static uint8, bitWidth: static uint8): FldVal[Taddr] {.inline.} =
  ## Returns the bit field masked and down shifted to the zero'th bit
  when bitWidth == 0: {.error: "bitWidth must be greater than zero".}
  when (bitOffset + bitWidth) > 32'u8: {.error: "bit field must not exceed register size in bits".}
  when defined(arm):
    {.emit: ["asm (\"ubfx %0, %1, %2, %3\"\n\t: \"=r\" (", result, ")\n\t: \"r\" (", regVal, "), \"n\" (", bitOffset, "), \"n\" (", bitWidth, "));\n"].}
  else: # for desktop testing
    const fldMask = 0xFFFFFFFF'u32 shr (32 - bitWidth)
    FldVal[Taddr]((regVal.uint32 shr bitOffset) and fldMask)

func getField[Taddr: static RegType](regVal: RegVal[Taddr], bitOffset: uint8, bitWidth: static uint8): FldVal[Taddr] {.inline.} =
  ## Returns the bit field masked and down shifted to the zero'th bit
  ## Non-static bitOffset, compared to the previous func
  when bitWidth == 0: {.error: "bitWidth must be greater than zero".}
  assert (bitOffset + bitWidth) < 32'u8, "bit field must not exceed register size in bits"
  when defined(arm):
    {.emit: ["asm (\"ubfx %0, %1, %2, %3\"\n\t: \"=r\" (", result, ")\n\t: \"r\" (", regVal, "), \"n\" (", bitOffset, "), \"n\" (", bitWidth, "));\n"].}
  else: # for desktop testing
    const fldMask = 0xFFFFFFFF'u32 shr (32 - bitWidth)
    FldVal[Taddr]((regVal.uint32 shr bitOffset) and fldMask)

func setField[Taddr: static RegType](fldVal: FldVal[Taddr], value: RegType, bitOffset: static uint8, bitWidth: static uint8): FldVal[Taddr] {.inline.} =
  ## Returns `fldVal` with `value` shifted and inserted into the given bit offset and width.
  ## No bits outside of the bit offset and width are changed.
  when bitWidth == 0: {.error: "bitWidth must be greater than zero".}
  when (bitOffset + bitWidth) > 32: {.error: "bit field must not exceed register size in bits".}
  when defined(arm):
    result = fldVal
    {.emit: ["asm (\"bfi %0, %1, %2, %3\"\n\t: \"+r\" (", result, ")\n\t: \"r\" (", value, "), \"n\" (", bitOffset, "), \"n\" (", bitWidth, "));\n"].}
  else: # for desktop testing
    const fldMask = 0xFFFFFFFF'u32 shr (32 - bitWidth) shl bitOffset
    let r = fldVal.uint32 and not fldMask
    FldVal[Taddr](r or ((value.uint32 shl bitOffset) and fldMask))

func setField[Taddr: static RegType](fldVal: FldVal[Taddr], value: RegType, bitOffset: uint8, bitWidth: static uint8): FldVal[Taddr] {.inline.} =
  ## Returns `fldVal` with `value` shifted and inserted into the given bit offset and width.
  ## No bits outside of the bit offset and width are changed.
  ## Non-static bitOffset, compared to the previous func
  when bitWidth == 0: {.error: "bitWidth must be greater than zero".}
  assert (bitOffset + bitWidth) < 32, "bit field must not exceed register size in bits"
  when defined(arm):
    result = fldVal
    {.emit: ["asm (\"bfi %0, %1, %2, %3\"\n\t: \"+r\" (", result, ")\n\t: \"r\" (", value, "), \"n\" (", bitOffset, "), \"n\" (", bitWidth, "));\n"].}
  else: # for desktop testing
    let fldMask = 0xFFFFFFFF'u32 shr (32 - bitWidth) shl bitOffset
    let r: uint32 = fldVal.uint32 and not fldMask
    FldVal[Taddr](r or ((value.uint32 shl bitOffset) and fldMask))

proc write*[Taddr: static RegType](value: FldVal[Taddr]) {.inline.} =
  ## Writes the value to the register (volatile store).
  ## Implements the write part of `PER.REG.FLD(v1).write`
  ## NOTE: write access is already enforced at compile time
  ## by the fieldName procs that produce FldVal.
  const regAddr = cast[ptr FldVal[Taddr]](Taddr)
  volatileStore(regAddr, value)

template declareField*(peripheralName: untyped, registerName: untyped, fieldName: untyped,
                       bitOffset: static uint8, bitWidth: static uint8, dim: static int = 0, dimIncrement: static int = 0,
                       readAccess: static bool, writeAccess: static bool, fieldDesc: static string): untyped =
  const isDimensioned = dim != 0 and dimIncrement != 0
  const regAddr: RegType = `peripheralName _ registerName`

  when isDimensioned:
    proc fieldName*[Taddr: static RegType](_: `peripheralName _ registerName _ RegAddr`[Taddr], index: static uint32, value: RegType) {.inline.} =
      ## Overwrites the register's field's bits with the value.
      ## Overwrites the bits outside the field with zeroes.
      ## Implements the FLD part of `PER.REG.FLD(1, value)`
      when not writeAccess:
        {.error: "Attempted write to a field without write access.".}
      when index >= dim and dim > 0:
        {.error: "Attempted dimensioned register access beyond its limit.".}
      const initVal = FldVal[Taddr](0'u32)
      const bitOff = index.uint8 * dimIncrement
      let v = setField[Taddr](initVal, value, bitOff, bitWidth)
      const regAddr = cast[ptr RegType](Taddr)
      volatileStore(regAddr, v.RegType)

    proc fieldName*[Taddr: static RegType](_: `peripheralName _ registerName _ RegAddr`[Taddr], index: uint32, value: RegType) {.inline.} =
      ## Non-static index, compared to the previous proc
      ## Overwrites the register's field's bits with the value (runtime index).
      ## Overwrites the bits outside the field with zeroes.
      ## Implements the FLD part of `PER.REG.FLD(n, value)`
      when not writeAccess:
        {.error: "Attempted write to a field without write access.".}
      assert index >= 0 and index < dim, "Attempted dimensioned register access beyond its limit."
      const initVal = FldVal[Taddr](0'u32)
      let bitOff = uint8(index) * dimIncrement.uint8
      let v = setField[Taddr](initVal, value, bitOff, bitWidth)
      const regPtr = cast[ptr RegType](Taddr)
      volatileStore(regPtr, v.RegType)

    proc fieldName*[Taddr: static RegType](regVal: `peripheralName _ registerName _ RegVal`[Taddr], index: static uint32): FldVal[Taddr] {.inline.} =
      ## Reads the field from the register value (static index)
      ## Implements the FLD part of `PER.REG.read().FLD(1)
      when not readAccess:
        {.error: "Attempted read from a field without read access.".}
      when index >= dim and dim > 0:
        {.error: "Attempted dimensioned register access beyond its limit.".}
      const bitOff = index.uint8 * dimIncrement
      getField[Taddr](regVal, bitOff, bitWidth)

    proc fieldName*[Taddr: static RegType](regVal: `peripheralName _ registerName _ RegVal`[Taddr], index: uint32): FldVal[Taddr] {.inline.} =
      ## Non-static index, compared to the previous proc
      ## Reads the field from the register value (runtime index).
      ## Implements the FLD part of `PER.REG.read().FLD(n)`
      when not readAccess:
        {.error: "Attempted read from a field without read access.".}
      let bitOff = uint8(index) * dimIncrement.uint8
      getField[Taddr](regVal, bitOff, bitWidth)

    proc fieldName*[Taddr: static RegType](inVal: `peripheralName _ registerName _ RegVal`[Taddr] | FldVal[Taddr], index: static uint32, value: RegType): FldVal[Taddr] {.inline.} =
      ## Rmw's the field's bits in the register with `value`.
      ## Returns the FldVal[Taddr] type to allow chaining of more field modifications.
      ## Implements the FLD part of `PER.REG.read().FLD(idx, value)`
      when not writeAccess:
        {.error: "Attempted write to a field without write access.".}
      when index >= dim:
        {.error: "Attempted write to a field index beyond its limit.".}
      let fldVal = cast[FldVal[Taddr]](inVal)
      const bitOff = index.uint8 * dimIncrement
      setField[Taddr](fldVal, value, bitOff, bitWidth)

  else: # not dimensioned
    proc fieldName*[Taddr: static RegType](_: `peripheralName _ registerName _ RegAddr`[Taddr], value: RegType) {.inline.} =
      ## Overwrites the register's field's bits with the value.
      ## Overwrites the bits outside the field with zeroes.
      ## Implements the FLD part of `PER.REG.FLD(1, value)`
      when not writeAccess:
        {.error: "Attempted write to a field without write access.".}
      const initVal = FldVal[Taddr](0'u32)
      let v = setField[Taddr](initVal, value, bitOffset, bitWidth)
      const regAddr = cast[ptr RegType](Taddr)
      volatileStore(regAddr, v.RegType)

    proc fieldName*[Taddr: static RegType](regVal: `peripheralName _ registerName _ RegVal`[Taddr]): FldVal[Taddr] {.inline.} =
      ## Reads the field from the register value
      ## Implements the FLD part of `PER.REG.read().FLD(1)
      when not readAccess:
        {.error: "Attempted read from a field without read access.".}
      getField[Taddr](regVal, bitOffset, bitWidth)

    proc fieldName*[Taddr: static RegType](inVal: `peripheralName _ registerName _ RegVal`[Taddr] | FldVal[Taddr], value: RegType): FldVal[Taddr] {.inline.} =
      ## Rmw's the field's bits in the register with `value`.
      ## Returns the FldVal[Taddr] type to allow chaining of more field modifications.
      ## Implements the FLD part of `PER.REG.read().FLD(idx, value)`
      when not writeAccess:
        {.error: "Attempted write to a field without write access.".}
      let fldVal = cast[FldVal[Taddr]](inVal)
      setField[Taddr](fldVal, value, bitOffset, bitWidth)

macro declareEnum(enumType: untyped, enumPairsStmtList: untyped) {.inject.} =
  ## Declares a Nim enumerator with the given type that is bound to a specific register.
  ## The enum names and values are from the SVD file.
  enumPairsStmtList.expectKind(nnkStmtList)
  var fields: seq[NimNode]
  for asgnNode in enumPairsStmtList:
    asgnNode.expectKind(nnkAsgn)
    var node = newNimNode(nnkEnumFieldDef)
    node.add(asgnNode[0])
    node.add(asgnNode[1])
    fields.add(node)
  newEnum(name = enumType, fields = fields, public = true, pure = true)  # TODO: are we sure we want pure?

macro declareFieldEnum*(peripheralName: untyped, registerName: untyped, fieldName: untyped,
                        bitOffset: static int, bitWidth: static int,
                        readAccess: static bool, writeAccess: static bool, values: untyped) {.inject.} =
  ## Declares an enum type for a register field and overloaded procs for type-safe access.
  ## When writeAccess: generates a field= overload accepting the enum type.
  ## When readAccess and writeAccess: generates RMW chain overloads accepting the enum type.
  let enumType = ident(peripheralName.strVal & '_' & registerName.strVal & '_' & fieldName.strVal & "_enum")
  let brandedAddrType = ident(peripheralName.strVal & '_' & registerName.strVal & "_RegAddr")
  let brandedAddrTypeInst = nnkBracketExpr.newTree(brandedAddrType, ident("Taddr"))
  let brandedValType = ident(peripheralName.strVal & '_' & registerName.strVal & "_RegVal")
  let brandedValTypeInst = nnkBracketExpr.newTree(brandedValType, ident("Taddr"))
  result = newStmtList()
  result.add(quote do:
    declareEnum(`enumType`, `values`))
  if writeAccess:
    result.add(quote do:
      proc `fieldName`*[Taddr: static RegType](regAddr: `brandedAddrTypeInst`, value: `enumType`) {.inline.} =
        `fieldName`(regAddr, value.uint32))
  if readAccess and writeAccess:
    result.add(quote do:
      proc `fieldName`*[Taddr: static RegType](regVal: `brandedValTypeInst`, value: `enumType`): FldVal[Taddr] {.inline.} =
        `fieldName`(regVal, value.uint32)
      proc `fieldName`*[Taddr: static RegType](chainVal: FldVal[Taddr], value: `enumType`): FldVal[Taddr] {.inline.} =
        `fieldName`(chainVal, value.uint32))
