#[
Metagenerator converts peripheral, register and field declarations into
symbols and procs that enable programmer-friendly, type-safe access
to Arm Cortex-M special function registers, their fields and enum values.
The declarations come from silicon vendors' SVD files that are
converted by minisvd2nim.

These templates and macros are not used by the minisvd2nim program.
Instead, the code generated by minisvd2nim calls these templates and macros.
This allows the programmer to change the final form of the Nim source
by editing this file and not changing or recompiling the minisvd2nim binary.

Two ARM assembly instructions, BFI and UBFX, are used in setField and
getField when compiling for ARM targets.  These instructions perform bit
field insert and extract operations in a single instruction.  The emitted
assembly syntax is for GCC inline assembly.  Here is a reference:
  https://gcc.gnu.org/onlinedocs/gcc/Using-Assembly-Language-with-C.html

Note: In this file, the term "mask" is defined as a word having its bits set
to ones in the positions of interest.  So, a field mask has ones set in the
field's bit positions.
]#
#!fmt: off

import std/[macros, tables, volatile]

type
  RegType = uint32
  # Encode the address into the Peripheral, RegAddr and *Val types so that
  # the address is available to the tail of the call chain (usually a write)
  Peripheral[Taddr: static RegType] = distinct RegType
  RegAddr[Taddr: static RegType] = distinct RegType
  RegVal[Taddr: static RegType] = distinct RegType
  FldVal[Taddr: static RegType] = distinct RegType

var
  # Store some attributes that are set within declare* templates
  # for use at compile time outside of their declare* templates
  regReadAccess {.compileTime.} = initTable[RegType, bool]()
  regWriteAccess {.compileTime.} = initTable[RegType, bool]()
  regIsDimensioned {.compileTime.} = initTable[RegType, bool]()
  regDim {.compileTime.} = initTable[RegType, RegType]()
  regDimIncrement {.compileTime.} = initTable[RegType, RegType]()

proc read*[Taddr: static RegType](_: RegAddr[Taddr], index: static int = 0): RegVal[Taddr] {.inline.} =
  ## Returns the value loaded from the index's offset from the register.
  ## Implements `PER.REG.read()`
  when not regReadAccess[Taddr]:
    {.error: "Attempted read from a register without read access.".}
  when index > 0 and not regIsDimensioned[Taddr]:
    {.error: "Attempted dimensioned access to a non-dimensioned register.".}
  when index >= regDim[Taddr] and regDim[Taddr] != 0:
    {.error: "Attempted write to a register index beyond its limit.".}
  const a: RegType = Taddr + index.RegType * regDimIncrement[Taddr]
  const r = cast[ptr RegVal[Taddr]](a)
  volatileLoad(r)

proc write*[Taddr: static RegType](_: RegAddr[Taddr], value: RegVal[Taddr] | RegType) {.inline.} =
  ## Writes the value to the register.
  ## Implements `PER.REG.write(value)`
  when not regWriteAccess[Taddr]:
    {.error: "Attempted write to a register without write access.".}
  const p = cast[ptr RegType](Taddr)
  volatileStore(p, value.RegType)

